package io.antmedia.settings;

public class EmailSettings {

	private String emailUsername = null;

	public String getEmailUsername() {
		return emailUsername;
	}

	public void setEmailUsername(String emailUsername) {
		this.emailUsername = emailUsername;
	}

	private String emailPassword = null;

	public String getEmailPassword() {
		return emailPassword;
	}

	public void setEmailPassword(String emailPassword) {
		this.emailPassword = emailPassword;
	}

	private String emailSmtpHost = null;

	public String getEmailSmtpHost() {
		return emailSmtpHost;
	}

	public void setEmailSmtpHost(String emailSmtpHost) {
		this.emailSmtpHost = emailSmtpHost;
	}

	private String emailSmtpEncryption = null;

	public String getEmailSmtpEncryption() {
		return emailSmtpEncryption;
	}

	public void setEmailSmtpEncryption(String emailSmtpEncryption) {
		this.emailSmtpEncryption = emailSmtpEncryption;
	}

	private String emailCheckDate = null;

	public String getEmailCheckDate() {
		return emailCheckDate;
	}

	public void setEmailCheckDate(String emailCheckDate) {
		this.emailCheckDate = emailCheckDate;
	}

}
